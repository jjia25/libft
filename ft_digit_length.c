#include "libft.h"

int	ft_digit_length(long n)
{
	int x;

	x = 0;
	if (n < 0)
		x++;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n /= 10;
		x++;
	}
	return (x);
}
