#include "libft.h"

void				*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*us;

	us = (unsigned char *)s;
	if (n == 0)
		return (s);
	while (n > 0)
	{
		*us = c;
		us++;
		n--;
	}
	return (s);
}
